
public class PrimitiveDataTypes {

	public static void main(String[] args) {
		
		boolean b = false; // 1 bit
		byte by = 1 ; // 1 byte
		short s = 2; // 2 bytes
		int i = 23; // 4 bytes 
		long l = 344l; // 8 bytes // By default number value is integer so to make it long we have to add l.
		float f = 24.1f; // 4 bytes // By default float value is double so to make it float type we have to add f.
		double d = 34.45; // 8  bytes
		 
		System.out.println(b +" " + by + " " + s + " " + i + " " + l + " " +f + " " + d + " " );
		
	}
}
